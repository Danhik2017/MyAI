def handle_help_command(text: str) -> str | None:
    lower = text.lower().strip()

    detailed_phrases = [
        "подробная помощь",
        "подробно что ты умеешь",
        "подробный список команд",
        "все команды",
        "полный список команд",
    ]

    if any(phrase in lower for phrase in detailed_phrases):
        return get_detailed_help_text()

    help_phrases = [
        "что ты умеешь",
        "покажи команды",
        "список команд",
        "какие команды",
        "какие функции доступны",
        "помощь",
        "help",
    ]

    if any(phrase in lower for phrase in help_phrases):
        return get_help_text()

    return None