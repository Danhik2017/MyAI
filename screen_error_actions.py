from vision import analyze_screen


def explain_screen_error() -> str:
    return analyze_screen(
        question=(
            "Посмотри на экран и найди ошибку, предупреждение, traceback, "
            "красный текст, сообщение IDE, консоль или окно с проблемой. "
            "Объясни, что именно сломалось, в каком файле или месте это видно, "
            "и что нужно сделать для исправления. "
            "Если ошибка связана с Python, объясни причину простыми словами. "
            "Если точный текст ошибки плохо виден, честно скажи, что не можешь разобрать его полностью. "
            "Ответь на русском языке, без markdown и без списков, как голосовой помощник."
        ),
        detailed=True,
    )


def explain_screen_code_problem() -> str:
    return analyze_screen(
        question=(
            "Посмотри на экран. Если там открыт код, консоль, терминал или IDE, "
            "найди возможную проблему в коде или в выводе программы. "
            "Объясни, что может быть не так, и предложи следующий шаг для проверки. "
            "Не выдумывай точный код, если он плохо виден. "
            "Ответь на русском языке, практично и понятно."
        ),
        detailed=True,
    )